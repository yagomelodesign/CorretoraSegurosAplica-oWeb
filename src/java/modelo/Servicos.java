public class Servicos {
    
    private Integer idServicos;
    private String tipoServico;
    private Integer valorServico;

    public Integer getIdServicos() {
        return idServicos;
    }

    public void setIdServicos(Integer idServicos) {
        this.idServicos = idServicos;
    }

    public String getTipoServico() {
        return tipoServico;
    }

    public void setTipoServico(String tipoServico) {
        this.tipoServico = tipoServico;
    }

    public Integer getValorServico() {
        return valorServico;
    }

    public void setValorServico(Integer valorServico) {
        this.valorServico = valorServico;
    }
    
    
    
}
